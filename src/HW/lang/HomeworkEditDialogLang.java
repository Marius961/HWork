	private String saveButton;
	private String cancelButton;
	
	public String getHomeworkLabel() {
		return homeworkLabel;
	}
	
	public void setHomeworkLabel(String homeworkLabel) {
		this.homeworkLabel = homeworkLabel;
	}
	
